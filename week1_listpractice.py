# Create a list of 5 subjects, print each one using a for loop

classSubjects = ["Math", "Computer Science", "History", "English", "Band", "Physical Education"]

for subject in classSubjects:
    print(subject)

# subject is the loop variable - created automatically by the loop, holds one subject at a time
# I looper directly over classSubjects instead of using range()
# Print is simple - print(subject) means print each subject variable we assigned to each item in the list