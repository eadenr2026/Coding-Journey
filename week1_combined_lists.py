# list of hobbies
hobbies = [
    "Video Games",
    "Music",
    "Coding",
    "Sports",
    "Learning"
]
# list of foods
foods = [
    "Pizza",
    "Chicken",
    "Steak",
    "Pasta",
    "Ice cream"
]
# combined list using + operator
hobbies_and_foods = hobbies + foods

# Loop through each item in list, and print string with list items
