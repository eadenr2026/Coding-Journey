# Write a program that stores 5 hobbies in a list and prints each one with 'Rowland likes:' in front

hobbies = [
    "Video Games",
    "Coding",
    "Music",
    "Animation",
    "Sports"
]

foods = [
    "Pizza",
    "Chicken",
    "Pasta",
    "Steak",
    "Ice cream"
]


for hobby in hobbies:
    print(f"Rowland likes: {hobby}")

print()

for food in foods:
    print(f"Rowland likes: {food}")

